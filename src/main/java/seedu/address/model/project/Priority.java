package seedu.address.model.project;

/**
 * Enumerates the priority levels for a task or item, typically used to indicate the importance or urgency.
 * It provides three priority levels: HIGH, MEDIUM, and LOW.
 */
public enum Priority {
    /**
     * Represents the highest priority level, indicating a task or item of utmost importance and urgency.
     */
    HIGH,

    /**
     * Represents a medium priority level, indicating a task or item with moderate importance and urgency.
     */
    MEDIUM,

    /**
     * Represents the lowest priority level, indicating a task or item with minimal importance and urgency.
     */
    LOW
}
